    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombreMascota = txtNombre.getText();
        String razaMascota = txtRaza.getText();
        String colorMascota = txtColor.getText();
        String observacionesMasco = txtObservacionesArea.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atencionEsp = (String) cmbAtencionEspecial.getSelectedItem();
        
        String nombre = txtNombreDuenio.getText();
        String telefonoDuenio = txtTelefono.getText();

        control.guardar(nombreMascota, razaMascota, colorMascota, observacionesMasco,
                alergico, atencionEsp, nombre, telefonoDuenio);
        
        JOptionPane infoPanel = new JOptionPane("Se guard√≥ correctamente!");
        infoPanel.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = infoPanel.createDialog("IMPORTANTE");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnGuardarActionPerformed


